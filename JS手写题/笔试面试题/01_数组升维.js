/**
 * 题目描述：
 * 输出的数组内不应有重复的内容
 * 按照数字从小到大排序
 * 数字尾数是4的被去除掉
 * 按照等长截为5个子数组（子数组长度尽量大，但必须等长）
 */

// 例1：输入
// [214, 12, 120, 110, 12, 155, 164, 143, 51, 55, 110, 550]
// [110, 11, 12, 120, 13, 14, 144, 143, 114, 51, 55, 550]
// 输出
// [ [ 11, 12 ], [ 13, 51 ], [ 55, 110 ], [ 120, 143 ], [ 155, 550 ] ]

// 例2：输入
// [214, 12, 120, 110, 12, 155, 164, 143, 51, 55, 110, 550, 233, 9, 68, 168, 71, 161]
// [110, 11, 12, 120, 13, 14, 144, 143, 114, 51, 55, 550, 214, 71, 84, 357, 199, 164, 258]
// 输出
// [
//   [ 9, 11, 12 ],
//   [ 13, 51, 55 ],
//   [ 68, 71, 110 ],
//   [ 120, 143, 155 ],
//   [ 161, 168, 199 ]
// ]

// 例3：输入
// [214, 12, 120, 110]
// [110, 11, 12, 120]
// 输出
// [ [], [], [], [], [] ]


let arr1 = [214, 12, 120, 110, 12, 155, 164, 143, 51, 55, 110, 550];
let arr2 = [110, 11, 12, 120, 13, 14, 144, 143, 114, 51, 55, 550];
arr1.sort((a, b) => a - b);
arr2.sort((a, b) => a - b);
let a1 = [...new Set(arr1)];
let a2 = [...new Set(arr2)];
let a = [...a1, ...a2];
a = [...new Set([...a1, ...a2])];
a.sort((a, b) => a - b);
for (let i = 0; i < a.length; i++) {
  if (a[i] % 10 === 4) {
    a.splice(i, 1);
    i--;
  }
}
a.length = a.length - (a.length % 5);
let step = a.length / 5;
for (let i = 0; i < a.length; i += step) {
  let tmp = [];
  for (let j = i; j < i +step; j++) tmp.push(a[j]);
  console.log(tmp);
  tmp = [];
}
